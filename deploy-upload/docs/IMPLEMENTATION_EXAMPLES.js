const errorHandling = `
Common Issues & Solutions:

1. Authentication Error (401)
   - Check JWT_SECRET matches frontend
   - Verify accessToken in cookies or Authorization header
   - Check user.isActive status (patients allowed inactive)

2. API Rate Limiting
   - Groq: 30 requests/minute free tier
   - Azure OpenAI: Check deployment TPM quota
   - Solution: Implement exponential backoff

3. Face Recognition Not Loading
   - Ensure models downloaded (~350MB)
   - Check camera permissions
   - Test in HTTPS (required for camera access)

4. Santé Connect OAuth Failure
   - Verify redirect URI matches exactly
   - Check client credentials
   - Ensure state token validation

5. Missing AI Responses
   - Check provider API keys
   - Verify model deployments exist
   - Check rate limits not exceeded
   - Review logs in VSCODE_TARGET_SESSION_LOG
`;

