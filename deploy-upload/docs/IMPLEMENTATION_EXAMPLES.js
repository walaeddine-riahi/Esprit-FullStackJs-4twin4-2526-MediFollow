// 6. FACE RECOGNITION LOGIN - Biometric authentication
// ============================================================================

const faceRecognitionExample = `
// Component: FaceLoginModal
import FaceLoginModal from '@/components/FaceLoginModal';

function LoginPage() {
  const [isOpen, setIsOpen] = useState(false);
  
  const handleFaceLogin = async (faceDescriptor, rawVideo) => {
    // Send to backend for verification
    const response = await fetch('/api/auth/verify-face', {
      method: 'POST',
      body: JSON.stringify({
        faceDescriptor,
        userId: 'known-user-id' // For registered users
      })
    });
    
    if (response.ok) {
      // Logged in successfully
      router.push('/dashboard');
    }
  };
  
  return (
    <>
      <button onClick={() => setIsOpen(true)}>
        Face Recognition Login
      </button>
      <FaceLoginModal 
        isOpen={isOpen}
        onClose={() => setIsOpen(false)}
        onSuccess={handleFaceLogin}
      />
    </>
  );
}
`;

